// ========== Template literals ==========

// ---------- ES5 ----------

var first = 'Gregory'
var second = 'Yanushkovsky'

console.log('My name is  ' + first + ' ' + second + '.')

// ---------- ES6 ----------

// In ECMAScript 6 there were added template literals. instead of ' or " you should use ` and ${} for the variable.

const first = 'Gregory'
const second = 'Yanushkovsky'

console.log(`My name is ${first} ${second}.`)